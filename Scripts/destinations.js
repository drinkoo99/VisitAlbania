// PAGE LOAD LOGIC
// ====================================
document.addEventListener("DOMContentLoaded", () => {
  const type = localStorage.getItem("destinationType");

  if (type === "mountains") {
    renderDestinations(mountains);
  } else {
    renderDestinations(beaches); // default = beaches
  }
});
